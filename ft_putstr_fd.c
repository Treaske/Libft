#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	len;

	if (!s || !fd)
		return ;
	len = 0;
	while (s[len] != '\0')
	{
		write(fd, &s[len], 1);
		len++;
	}
}
