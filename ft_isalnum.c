#include "libft.h"

int	ft_isalnum(int leter)
{
	if (leter >= 65 && leter <= 122)
	{
		if (leter >= 91 && leter <= 96)
			return (0);
		else
			return (1);
	}
	else if (leter >= 48 && leter <= 57)
		return (1);
	return (0);
}
